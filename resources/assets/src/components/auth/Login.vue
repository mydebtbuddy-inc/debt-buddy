<script>
import { required, minLength, maxLength, requiredIf } from 'vuelidate/lib/validators'
import LaddaBtn from '@/vendor/libs/ladda/Ladda'

export default {
  name: 'login',
  metaInfo: {
    title: 'Login'
  },
  components: {
    LaddaBtn
  },
  data: () => ({
    token: localStorage.getItem('token') || null,
    loading: false,
    loginError: null,
    user: {
      email: '',
      password: '',
      rememberMe: false
    }
  }),
  validations: {
    user: {
      email: {
        required,
        minLength: minLength(4),
        maxLength: maxLength(128)
      },
      password: {
        required: required,
        minLength: minLength(4)
      }
    }
  },
  methods: {
    submitForm() {
      $("#submit_form").click();
    },
    login() {
      this.loginError = null
      this.loading = true

      this.$store.dispatch('generateToken', {
        username: this.user.email,
        password: this.user.password
      })
      .then(response => {
        setTimeout(() => {
          this.$router.push({name: 'dashboard'})
        }, 500)
      })
      .catch(error => {
        console.log(error)
        this.loginError = error.response.data
        this.loading = false
      })
    }
  }
}
</script>
